create table if not exists activity_execution_segments (
  id bigint generated by default as identity primary key,
  planning_item_id bigint not null references planning_items(id) on delete cascade,
  activity_group_id text not null,
  item_date date not null,
  shift text not null check (shift in ('Dia', 'Noche')),
  start_time time not null,
  end_time time not null,
  level text not null,
  front text not null,
  category text not null check (category in ('actividad', 'interferencia')),
  item_type text not null,
  description text not null,
  notes text null,
  segment_order integer not null default 1,
  created_by uuid references profiles(user_id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint activity_execution_segments_valid_range check (
    start_time <> end_time
    and (
      (
        shift = 'Dia'
        and start_time >= time '08:00'
        and end_time <= time '20:00'
        and end_time > start_time
      )
      or (
        shift = 'Noche'
        and (
          (start_time >= time '20:00' and end_time > start_time)
          or (start_time >= time '20:00' and end_time <= time '08:00')
          or (start_time <= time '08:00' and end_time <= time '08:00' and end_time > start_time)
        )
      )
    )
  )
);

