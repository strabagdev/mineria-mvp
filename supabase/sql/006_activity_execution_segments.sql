insert into activity_execution_segments (
  planning_item_id,
  activity_group_id,
  item_date,
  shift,
  start_time,
  end_time,
  level,
  front,
  category,
  item_type,
  description,
  notes,
  segment_order,
  created_by,
  created_at,
  updated_at
)
select
  planned.id,
  real.activity_group_id,
  real.item_date,
  real.shift,
  real.start_time,
  real.end_time,
  real.level,
  real.front,
  real.category,
  real.item_type,
  real.description,
  real.notes,
  1,
  real.created_by,
  real.created_at,
  real.updated_at
from planning_items real
join planning_items planned
  on planned.activity_group_id = real.activity_group_id
 and planned.tracking_type = 'programado'
where real.tracking_type = 'real'
on conflict do nothing;

